function convertToHTML(text) {
    const paragraphs = text.split('\n\n').filter(p => p.trim());
    const html = [];
    
    for (let i = 0; i < paragraphs.length; i++) {
        const p = paragraphs[i].trim();
        
        // Заголовки
        if (p.match(/^[А-Я][^.!?]*$/)) {
            const prevP = i > 0 ? paragraphs[i - 1].trim() : '';
            const nextP = i < paragraphs.length - 1 ? paragraphs[i + 1].trim() : '';
            
            // Если это короткий текст без знаков препинания и следующий абзац начинается с маленькой буквы - это заголовок
            if (!p.includes('.') && !p.includes(',') && !p.includes('?') && !p.includes('!') && 
                p.length < 100 && (nextP.length === 0 || nextP[0] === nextP[0].toLowerCase())) {
                html.push(`<h2>${p}</h2>`);
                continue;
            }
        }
        
        // Списки
        if (p.includes(';') && (p.includes('—') || p.includes(':'))) {
            const parts = p.split(';').filter(part => part.trim());
            if (parts.length > 1) {
                html.push('<ul>');
                parts.forEach(part => {
                    const trimmed = part.trim();
                    if (trimmed) {
                        html.push(`<li>${trimmed}</li>`);
                    }
                });
                html.push('</ul>');
                continue;
            }
        }
        
        // Обычные параграфы
        let formatted = p;
        
        // Жирный текст для подзаголовков
        if (p.match(/^[А-Я][^.!?]*[.:]$/)) {
            formatted = `<p><strong>${p}</strong></p>`;
        } else {
            formatted = `<p>${p}</p>`;
        }
        
        html.push(formatted);
    }
    
    return html.join('');
}

